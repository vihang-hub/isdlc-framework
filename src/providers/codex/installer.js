/**
 * Remove Codex-specific generated files.
 * Preserves user-modified content.
 *
 * @param {string} projectRoot - Target project directory
 * @param {Object} [options={}] - Uninstall options
 * @returns {Promise<{ success: boolean, filesRemoved: string[], filesPreserved: string[], errors: string[] }>}
 */
export async function uninstallCodex(projectRoot, options = {}) {
  const filesRemoved = [];
  const filesPreserved = [];
  const errors = [];

  try {
    const config_obj = getCodexConfig();
    const paths = getProjectionPaths();
    const configPath = join(projectRoot, paths.providerConfig);
    const config = readConfigMeta(configPath);
    const storedHashes = config.fileHashes || {};
    const codexDir = join(projectRoot, config_obj.frameworkDir);

    for (const [key, relativePath] of Object.entries(paths)) {
      if (key === 'providerConfig') continue; // Remove config last

      const fullPath = join(projectRoot, relativePath);
      if (!existsSync(fullPath)) continue;

      const currentContent = readFileSync(fullPath, 'utf-8');
      const currentHash = contentHash(currentContent);
      const storedHash = storedHashes[relativePath];

      if (storedHash && currentHash !== storedHash) {
        // User modified — preserve
        filesPreserved.push(relativePath);
      } else {
        // Unmodified or no hash — remove
        rmSync(fullPath);
        filesRemoved.push(relativePath);
      }
    }

    // Remove config.json
    if (existsSync(configPath)) {
      rmSync(configPath);
      filesRemoved.push(paths.providerConfig);
    }

    // Remove .codex/ if empty
    removeIfEmpty(codexDir);

  } catch (err) {
    errors.push(err.message);
  }

  return {
    success: errors.length === 0,
    filesRemoved,
    filesPreserved,
    errors
  };
}

