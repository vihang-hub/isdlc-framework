/**
 * Validate Codex installation health.
 *
 * @param {string} projectRoot - Project directory
 * @returns {Promise<{ healthy: boolean, checks: Array<{ name: string, passed: boolean, message: string }> }>}
 */
export async function doctorCodex(projectRoot) {
  const checks = [];

  const config = getCodexConfig();
  const paths = getProjectionPaths();
  const codexDir = join(projectRoot, config.frameworkDir);

  // Check 1: .codex/ directory exists
  const dirExists = existsSync(codexDir);
  checks.push({
    name: 'codex-directory-exists',
    passed: dirExists,
    message: dirExists ? '.codex/ directory exists' : '.codex/ directory missing'
  });

  if (!dirExists) {
    return { healthy: false, checks };
  }

  // Check 2: config.json present and valid
  const configPath = join(projectRoot, paths.providerConfig);
  let configValid = false;
  if (existsSync(configPath)) {
    try {
      const parsed = JSON.parse(readFileSync(configPath, 'utf-8'));
      configValid = parsed.provider === 'codex';
    } catch {
      configValid = false;
    }
  }
  checks.push({
    name: 'config-json-valid',
    passed: configValid,
    message: configValid ? 'config.json is valid' : 'config.json missing or invalid'
  });

  // Check 3: Instruction files exist
  let allFilesPresent = true;
  for (const [key, relativePath] of Object.entries(paths)) {
    if (key === 'providerConfig') continue;
    const fullPath = join(projectRoot, relativePath);
    const fileExists = existsSync(fullPath);
    if (!fileExists) {
      allFilesPresent = false;
    }
    checks.push({
      name: `file-${key}`,
      passed: fileExists,
      message: fileExists ? `${relativePath} exists` : `${relativePath} missing`
    });
  }

  const healthy = checks.every(c => c.passed);
  return { healthy, checks };
}
