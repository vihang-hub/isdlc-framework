/**
 * Codex Provider Installer — Codex-Specific Installation Logic
 * ==============================================================
 * REQ-0115: Codex Installation and Doctor Paths
 *
 * Implements install/update/uninstall/doctor for the Codex provider.
 * Simpler than the Claude installer: no hooks, no settings.json merge,
 * no symlinks. Uses the same fs-helpers imports.
 *
 * API signature parity with Claude installer (FR-005).
 *
 * @module src/providers/codex/installer
 */

import { join } from 'node:path';
import { createHash } from 'node:crypto';
import {
  existsSync, mkdirSync, writeFileSync, readFileSync,
  readdirSync, rmSync, statSync
} from 'node:fs';

import { getCodexConfig, getProjectionPaths } from './projection.js';

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

/**
 * Compute SHA-256 hash of a string.
 * @param {string} content
 * @returns {string}
 */
function contentHash(content) {
  return createHash('sha256').update(content, 'utf-8').digest('hex');
}

/**
 * Generate default content for a projection path file.
 * @param {string} key - Projection path key
 * @returns {string} Generated content
 */
function generateContent(key) {
  switch (key) {
    case 'instructions':
      return '# Codex Agents\n\nGenerated by iSDLC Codex provider adapter.\n\nThis file contains agent instructions for Codex tasks.\n';
    case 'teamSpec':
      return '# Team Specification\n\nGenerated by iSDLC Codex provider adapter.\n\nTeam structure and roles for the current project.\n';
    case 'contentModel':
      return '# Content Model\n\nGenerated by iSDLC Codex provider adapter.\n\nContent classification and section mappings.\n';
    case 'skillManifest':
      return '# Skills\n\nGenerated by iSDLC Codex provider adapter.\n\nSkill definitions and injection bindings.\n';
    default:
      return '';
  }
}

/**
 * Read the config.json metadata (content hashes for generated files).
 * @param {string} configPath
 * @returns {Object}
 */
function readConfigMeta(configPath) {
  try {
    if (existsSync(configPath)) {
      return JSON.parse(readFileSync(configPath, 'utf-8'));
    }
  } catch {
    // Fall through
  }
  return {};
}

/**
 * Write the config.json with metadata.
 * @param {string} configPath
 * @param {Object} config
 */
function writeConfigMeta(configPath, config) {
  writeFileSync(configPath, JSON.stringify(config, null, 2), 'utf-8');
}

/**
 * Remove a directory if it is empty.
 * @param {string} dirPath
 */
function removeIfEmpty(dirPath) {
  try {
    if (existsSync(dirPath) && statSync(dirPath).isDirectory()) {
      const entries = readdirSync(dirPath);
      if (entries.length === 0) {
        rmSync(dirPath, { recursive: true });
      }
    }
  } catch {
    // Non-fatal
  }
}

