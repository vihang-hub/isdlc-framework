// installCodex
// ---------------------------------------------------------------------------

/**
 * Install Codex-specific framework files into a project.
 *
 * Creates .codex/ with instruction files and provider config.
 *
 * @param {string} projectRoot - Target project directory
 * @param {Object} [options={}] - Installation options
 * @returns {Promise<{ success: boolean, filesCreated: string[], errors: string[] }>}
 */
export async function installCodex(projectRoot, options = {}) {
  const filesCreated = [];
  const errors = [];

  try {
    const config = getCodexConfig();
    const paths = getProjectionPaths();
    const codexDir = join(projectRoot, config.frameworkDir);
    const configPath = join(projectRoot, paths.providerConfig);

    // 1. Create .codex/ directory
    if (!existsSync(codexDir)) {
      mkdirSync(codexDir, { recursive: true });
    }

    // 2. Generate content hashes for tracking
    const fileHashes = {};

    // 3. Generate instruction files
    for (const [key, relativePath] of Object.entries(paths)) {
      if (key === 'providerConfig') continue; // config.json handled separately

      const fullPath = join(projectRoot, relativePath);
      const content = generateContent(key);

      if (content) {
        writeFileSync(fullPath, content, 'utf-8');
        fileHashes[relativePath] = contentHash(content);
        filesCreated.push(relativePath);
      }
    }

    // 4. Write config.json with provider info and content hashes
    const configData = {
      provider: 'codex',
      frameworkVersion: '0.1.0-alpha',
      generatedAt: new Date().toISOString(),
      fileHashes
    };
    writeConfigMeta(configPath, configData);
    filesCreated.push(paths.providerConfig);

  } catch (err) {
    errors.push(err.message);
  }

  return {
    success: errors.length === 0,
    filesCreated,
    errors
  };
}

