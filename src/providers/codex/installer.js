// updateCodex
// ---------------------------------------------------------------------------

/**
 * Update Codex instruction files from latest core models.
 * Preserves user-modified content (detects edits via content hash comparison).
 *
 * @param {string} projectRoot - Target project directory
 * @param {Object} [options={}] - Update options
 * @returns {Promise<{ success: boolean, filesUpdated: string[], filesSkipped: string[], errors: string[] }>}
 */
export async function updateCodex(projectRoot, options = {}) {
  const filesUpdated = [];
  const filesSkipped = [];
  const errors = [];

  try {
    const paths = getProjectionPaths();
    const configPath = join(projectRoot, paths.providerConfig);
    const config = readConfigMeta(configPath);
    const oldHashes = config.fileHashes || {};
    const newHashes = { ...oldHashes };

    for (const [key, relativePath] of Object.entries(paths)) {
      if (key === 'providerConfig') continue;

      const fullPath = join(projectRoot, relativePath);
      const newContent = generateContent(key);
      if (!newContent) continue;

      // Check if user modified the file
      if (existsSync(fullPath)) {
        const currentContent = readFileSync(fullPath, 'utf-8');
        const currentHash = contentHash(currentContent);
        const storedHash = oldHashes[relativePath];

        if (storedHash && currentHash !== storedHash) {
          // User modified — skip
          filesSkipped.push(relativePath);
          continue;
        }
      }

      // Regenerate
      writeFileSync(fullPath, newContent, 'utf-8');
      newHashes[relativePath] = contentHash(newContent);
      filesUpdated.push(relativePath);
    }

    // Update config.json
    writeConfigMeta(configPath, {
      ...config,
      fileHashes: newHashes,
      updatedAt: new Date().toISOString()
    });

  } catch (err) {
    errors.push(err.message);
  }

  return {
    success: errors.length === 0,
    filesUpdated,
    filesSkipped,
    errors
  };
}

